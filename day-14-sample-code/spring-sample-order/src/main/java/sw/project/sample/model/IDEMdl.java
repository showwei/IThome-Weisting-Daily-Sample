package sw.project.sample.model;

public class IDEMdl {
    String id;
    String name;
    String description;

    public String getId() {
        return id;
    }

    public IDEMdl setId(String id) {
        this.id = id;
        return this;
    }

    public String getName() {
        return name;
    }

    public IDEMdl setName(String name) {
        this.name = name;
        return this;
    }

    public String getDescription() {
        return description;
    }

    public IDEMdl setDescription(String description) {
        this.description = description;
        return this;
    }
}
